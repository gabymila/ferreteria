    private void btncrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearActionPerformed
        // TODO add your handling code here:
       int idcat,op,peso;
       String nomp,color, medida,stock,estado;
       double prevent;
        try {
           idcat = Integer.parseInt(txtcat.getText());
            nomp = txtnombre.getText();
            color = txtcolor.getText();
            medida = txtmedida.getText();
            stock = txtstock.getText();
            estado = txtestado.getText();
            prevent = Double.parseDouble(txtprec_vent.getText());
            peso = Integer.parseInt(txtpeso.getText());
            p = new Producto(idp, idcat, nomp, color, medida, peso, stock, estado, prevent);
            op = ipa.CrearProducto(p);
            limpiarComponentes();
            if(op==1){
                JOptionPane.showMessageDialog(null, "Registro Guardado");
                LimpiarJTable();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Registro no Guardado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e+"HOLA");
        }
       
    }//GEN-LAST:event_btncrearActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
      if(tblistas.getSelectedRow()>=0){
            int fila = tblistas.getSelectedRow();
            int id = (int) tblistas.getValueAt(fila, 1);
            int op = ipa.EliminarProducto(id);
            if(op==1){
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
                LimpiarJTable();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Registro no Eliminado");
            }
        }else{
        JOptionPane.showMessageDialog(null, "Seleccionar Registro");
        }   
    }//GEN-LAST:event_btneliminarActionPerformed

