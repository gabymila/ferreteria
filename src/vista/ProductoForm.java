    public final void cargarTabla(){
 dtm = (DefaultTableModel) tblistas.getModel();
 lista2 = ipa.listarProducto();
 Object []datos = new Object[10];
 for(int i=0;i<lista2.size();i++){
     datos[0]=i+1;
     datos[1]=lista2.get(i).getIdprod();
     datos[2]=lista2.get(i).getIdcat();
     datos[3]=lista2.get(i).getNomprod();
     datos[4]=lista2.get(i).getColor();
    datos[5]=lista2.get(i).getMedida();
     datos[6]=lista2.get(i).getPeso();
     datos[7]=lista2.get(i).getStock();
     datos[8]=lista2.get(i).getEstado();
     datos[9]=lista2.get(i).getPrec_venta();
     
dtm.addRow(datos);
 }
tblistas.setModel(dtm);
}

