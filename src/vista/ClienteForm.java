    private void btncrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncrearActionPerformed
        // TODO add your handling code here:
        int op;
        String nomp,apellid, dni, direccion, celu;
        try {
            nomp = txtnomb.getText();
            apellid = txtapellido.getText();
            dni = txtdni.getText();
            direccion = txtdireccion.getText();
            celu= txtcelu.getText(); 
            c = new Cliente(idp, nomp, apellid, dni,direccion, celu);
            op = ipa.CrearCliente(c);
            limpiarComponentes();
            if(op==1){
                JOptionPane.showMessageDialog(null, "Registro Guardado");
                LimpiarJTable();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Registro no Guardado");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e+"HOLA");
        }
    }//GEN-LAST:event_btncrearActionPerformed

    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // TODO add your handling code here:
        int op;
        String nomp,apellid, dni, direccion, celu;
        if(listarcliente.getSelectedRow()>=0){
            nomp = txtnomb.getText();
            apellid = txtapellido.getText();
            dni = txtdni.getText();
            direccion = txtdireccion.getText();
            celu= txtcelu.getText(); 
            c = new Cliente(idp, nomp, apellid, dni,direccion, celu);
            op = ipa.EditarCliente(c);
            limpiarComponentes();
            if(op==1){
                JOptionPane.showMessageDialog(null, "Registro Modificado");
                LimpiarJTable();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Registro no Modificado");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccionar Registro");
        }
    }//GEN-LAST:event_btneditarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
         if(listarcliente.getSelectedRow()>=0){
            int fila = listarcliente.getSelectedRow();
            int id = (int) listarcliente.getValueAt(fila, 1);
            int op = ipa.EliminarCliente(id);
            if(op==1){
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
                LimpiarJTable();
                cargarTabla();
            }else{
                JOptionPane.showMessageDialog(null, "Registro no Eliminado");
            }
        }else{
        JOptionPane.showMessageDialog(null, "Seleccionar Registro");
        }
    }//GEN-LAST:event_btneliminarActionPerformed

