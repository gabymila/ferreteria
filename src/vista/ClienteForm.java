    private void cargarTabla() {
 lista = ipa.listarCliente();
 Object []datos = new Object[6];
 for(int i=0;i<lista.size();i++){
     datos[0]=i+1;
     datos[1]=lista.get(i).getIdclien();
     datos[2]=lista.get(i).getNombre();
     datos[3]=lista.get(i).getApellido();
     datos[4]=lista.get(i).getDni();
     datos[5]=lista.get(i).getDireccion();
     datos[6]=lista.get(i).getCelu();
dtm.addRow(datos);
 }
listarcliente.setModel(dtm); 
    }

    private void limpiarComponentes() {
    txtnomb.setText(null);
    txtapellido.setText(null);
    txtdni.setText(null);
    txtdireccion.setText(null);
    txtcelu.setText(null);
    
    }

    private void LimpiarJTable() {
            int a = dtm.getRowCount()-1;  //Índices van de 0 a n-1
         for(int i=a;i>=0;i--){  
            dtm.removeRow(i);
        }
    }
